def eval_pairs(
    min_confidence: str = "medium",
    split: str | None = "test",
) -> list[EvalPair]:
    """Return eval pairs filtered by minimum confidence tier and split.

    Parameters
    ----------
    min_confidence : str
        Minimum confidence level ("low", "medium", "high").
    split : str | None
        If "train" or "test", filter to that split. If None, return all.
    """
    levels = {"low": 0, "medium": 1, "high": 2}
    threshold = levels.get(min_confidence, 1)
    pairs = [p for p in EVAL_PAIRS if levels.get(p.confidence, 0) >= threshold]
    if split is not None:
        pairs = [p for p in pairs if p.split == split]
    return pairs
