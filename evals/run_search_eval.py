#!/usr/bin/env python3
"""Evaluate hybrid search quality via NDCG@K — multi-category v2.

Reads the labelled query set from ``evals/search_eval_queries.jsonl`` and
scores ``/search/hybrid`` per-query, per-category, and corpus-wide.

The eval set is generated by ``build_eval_set.py`` from authority-controlled
fields in the corpus (Pleiades IDs, Trismegistos IDs, date_approx buckets,
findspot strings) plus a lexical baseline. See ``evals/README.md`` for the
methodology.

Usage:
    python evals/run_search_eval.py [--api-url https://api.openetruscan.com]
    python evals/run_search_eval.py --json   # machine-readable output
    python evals/run_search_eval.py --gate macro_mean=0.40,place_pleiades=0.30

Output:
    Per-query NDCG@K, per-category mean, macro mean across categories,
    and (with --json) a structured report. Exits non-zero if any --gate
    expression evaluates below the requested threshold.
"""

from __future__ import annotations

import argparse
import json
import math
import statistics
import sys
import time
from collections import defaultdict
from pathlib import Path
from typing import Any

import httpx

EVAL_FILE = Path(__file__).parent / "search_eval_queries.jsonl"
K = 10
# Per-category gates by default. After the structured-query parser
# landed on top of the FTS widening, the May-2026 prod baseline is:
#
#   chronology      mean=1.0000  n=3   (every period query maps to a date range)
#   cross_corpus    mean=1.0000  n=1   (trismegistos token → has_trismegistos)
#   place_pleiades  mean=0.7939  median=1.0000  n=19
#   place_findspot  mean=0.3912  median=0.0649  n=8
#   lexical         mean=0.3242  median=0.2201  n=40
#   macro_mean=0.7019, micro_mean=0.4955
#
# Gates sit well below those baselines to catch material regressions
# without false-failing on noise. chronology and cross_corpus are gated
# tightly (0.80) because they're now solved via structured parsing —
# any drop signals a regression in the parser or the search filters.
DEFAULT_GATE = (
    "lexical=0.25,place_pleiades=0.50,place_findspot=0.20,"
    "chronology=0.80,cross_corpus=0.80,macro_mean=0.50"
)

# Prod rate-limits /search/hybrid at 60/min (slowapi). One request per second
# stays comfortably under that. A full eval (~70 queries) takes ~80 s.
PER_REQUEST_DELAY_S = 1.05
RETRY_AFTER_429_S = 30.0


def dcg(relevances: list[float], k: int = K) -> float:
    return sum(rel / math.log2(i + 2) for i, rel in enumerate(relevances[:k]))


def ndcg(relevances: list[float], ideal: list[float], k: int = K) -> float:
    idcg = dcg(sorted(ideal, reverse=True), k)
    if idcg == 0:
        return 0.0
    return dcg(relevances, k) / idcg


def _query_ndcg(api_url: str, query_text: str, relevant: set[str]) -> float | None:
    """One query → one NDCG@K. Retries once on 429."""
    for attempt in (1, 2):
        try:
            resp = httpx.get(
                f"{api_url}/search/hybrid",
                params={"q": query_text, "limit": K},
                timeout=15.0,
            )
            if resp.status_code == 429 and attempt == 1:
                print(
                    f"  WAIT  429 on {query_text!r}, sleeping {RETRY_AFTER_429_S}s",
                    file=sys.stderr,
                )
                time.sleep(RETRY_AFTER_429_S)
                continue
            resp.raise_for_status()
            results = resp.json().get("results", [])
        except Exception as exc:
            print(f"  SKIP  {query_text!r}: {exc}", file=sys.stderr)
            return None

        rels = [1.0 if r.get("id") in relevant else 0.0 for r in results]
        ideal = [1.0] * len(relevant)
        return ndcg(rels, ideal)
    return None


def evaluate(api_url: str) -> dict[str, Any]:
    """Run the full eval. Returns a structured report.

    The shape:
    {
        "per_query": [{"query","category","ndcg"} ...],
        "per_category": {"category": {"mean","n","queries":[...]}, ...},
        "macro_mean": float,        # average over category means
        "micro_mean": float,        # average over individual queries
        "skipped": int,
    }
    """
    queries = [json.loads(line) for line in EVAL_FILE.read_text().splitlines() if line.strip()]

    per_query: list[dict[str, Any]] = []
    per_category: dict[str, list[float]] = defaultdict(list)
    skipped = 0

    for q in queries:
        query_text = q["query"]
        category = q.get("category", "uncategorised")
        relevant = set(q["relevant_ids"])

        score = _query_ndcg(api_url, query_text, relevant)
        if score is None:
            skipped += 1
            continue

        per_query.append({"query": query_text, "category": category, "ndcg": score})
        per_category[category].append(score)
        time.sleep(PER_REQUEST_DELAY_S)

    cat_summary = {
        cat: {
            "mean": statistics.fmean(scores) if scores else 0.0,
            "median": statistics.median(scores) if scores else 0.0,
            "n": len(scores),
        }
        for cat, scores in per_category.items()
    }
    macro = (
        statistics.fmean(c["mean"] for c in cat_summary.values()) if cat_summary else 0.0
    )
    micro = (
        statistics.fmean(p["ndcg"] for p in per_query) if per_query else 0.0
    )

    return {
        "per_query": per_query,
        "per_category": cat_summary,
        "macro_mean": macro,
        "micro_mean": micro,
        "skipped": skipped,
    }


def _print_human(report: dict[str, Any]) -> None:
    by_cat: dict[str, list[float]] = defaultdict(list)
    for p in report["per_query"]:
        by_cat[p["category"]].append(p["ndcg"])

    for cat, scores in sorted(by_cat.items()):
        print(f"\n[{cat}]  n={len(scores)}  mean={statistics.fmean(scores):.4f}")
        for p in [p for p in report["per_query"] if p["category"] == cat]:
            print(f"  {p['ndcg']:.3f}  {p['query']}")

    print()
    print(f"Macro mean (avg over category means): {report['macro_mean']:.4f}")
    print(f"Micro mean (avg over all queries):     {report['micro_mean']:.4f}")
    print(f"Skipped:                               {report['skipped']}")


def _evaluate_gates(report: dict[str, Any], gate_spec: str) -> tuple[bool, list[str]]:
    """Parse `--gate name=val,name2=val2` and decide pass/fail.

    Recognised names: ``macro_mean``, ``micro_mean``, or any category name.
    """
    cat_means = {cat: c["mean"] for cat, c in report["per_category"].items()}
    metrics = {
        "macro_mean": report["macro_mean"],
        "micro_mean": report["micro_mean"],
        **cat_means,
    }

    failures: list[str] = []
    for clause in gate_spec.split(","):
        clause = clause.strip()
        if not clause:
            continue
        try:
            name, value = clause.split("=")
            threshold = float(value)
        except ValueError:
            failures.append(f"unparseable gate clause {clause!r}")
            continue
        actual = metrics.get(name)
        if actual is None:
            failures.append(f"unknown gate metric {name!r}")
            continue
        if actual < threshold:
            failures.append(
                f"{name}={actual:.4f} below threshold {threshold:.2f}"
            )
    return (not failures), failures


def main() -> int:
    parser = argparse.ArgumentParser(description="Hybrid search NDCG@K eval")
    parser.add_argument("--api-url", default="http://localhost:8000")
    parser.add_argument("--json", action="store_true", help="emit machine-readable JSON")
    parser.add_argument(
        "--gate",
        default=DEFAULT_GATE,
        help=(
            "comma-separated `metric=threshold` pairs. The job exits non-zero if any "
            "metric falls below its threshold. Metrics: macro_mean, micro_mean, or "
            "any category name (e.g. 'place_pleiades=0.30')."
        ),
    )
    args = parser.parse_args()

    report = evaluate(args.api_url)

    if args.json:
        json.dump(report, sys.stdout, indent=2, ensure_ascii=False)
        sys.stdout.write("\n")
    else:
        _print_human(report)

    ok, failures = _evaluate_gates(report, args.gate)
    if not ok:
        for f in failures:
            print(f"FAIL: {f}", file=sys.stderr)
        return 1
    # PASS goes to stderr so `--json | jq` pipelines stay clean.
    print("\nPASS", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
